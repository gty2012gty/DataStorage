public class ForgotInfo {
    private String forgotUser;
    private String forgotPsassword;

    public String getForgotUser() {
        return forgotUser;
    }

    public void setForgotUser(String forgotUser) {
        this.forgotUser = forgotUser;
    }

    public String getForgotPsassword() {
        return forgotPsassword;
    }

    public void setForgotPsassword(String forgotPsassword) {
        this.forgotPsassword = forgotPsassword;
    }
}
